s = 'hello ooowooorld'
#Primeira letra maiscula
print(s.capitalize())

#todas as letras maiusculas
print(s.upper())

#todas as letras minusculas
print(s.lower())

#Contas quantas letras
print(s.count('o'))

#primeira ocorrencia de letra
print(s.find('w'))

#se todas sao maisculas
print(s.islower())

#se todas sao minusculas
print(s.isupper())

#se sao numeros
print(s.isalnum())

#se termina com letra espeficica 
print(s.endswith('d'))

#quebra aonde tem determinada letra
print(s.split('e'))

